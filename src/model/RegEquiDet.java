package entities;

public class RegEquiDet {

    public int CodEquiDet;
    public int CodEquivalencia;
    public String Instituicao;
    public String Disciplina;
    public Float Carga;
    public int Credito;
    public int Ano;
    public String Nota;

    public RegEquiDet() {
    }

    public int getCodEquiDet() {
        return CodEquiDet;
    }

    public void setCodEquiDet(int CodEquiDet) {
        this.CodEquiDet = CodEquiDet;
    }

    public int getCodEquivalencia() {
        return CodEquivalencia;
    }

    public void setCodEquivalencia(int CodEquivalencia) {
        this.CodEquivalencia = CodEquivalencia;
    }

    public String getInstituicao() {
        return Instituicao;
    }

    public void setInstituicao(String Instituicao) {
        this.Instituicao = Instituicao;
    }

    public String getDisciplina() {
        return Disciplina;
    }

    public void setDisciplina(String Disciplina) {
        this.Disciplina = Disciplina;
    }

    public Float getCarga() {
        return Carga;
    }

    public void setCarga(Float Carga) {
        this.Carga = Carga;
    }

    public int getCredito() {
        return Credito;
    }

    public void setCredito(int Credito) {
        this.Credito = Credito;
    }

    public int getAno() {
        return Ano;
    }

    public void setAno(int Ano) {
        this.Ano = Ano;
    }

    public String getNota() {
        return Nota;
    }

    public void setNota(String Nota) {
        this.Nota = Nota;
    }
    
    
}
