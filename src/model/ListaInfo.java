package entities;

public class ListaInfo {

    private int codLista;
    private Cadastrodisciplina nomeDis;

    public ListaInfo() {
    }

    public int getCodLista() {
        return codLista;
    }

    public void setCodLista(int codLista) {
        this.codLista = codLista;
    }

    public Cadastrodisciplina getNomeDis() {
        return nomeDis;
    }

    public void setNomeDis(Cadastrodisciplina nomeDis) {
        this.nomeDis = nomeDis;
    }

}
