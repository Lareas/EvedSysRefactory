package entities;

public class RegCrudMS {

    private int cms;
    private int cadastroAlunoDisciplina;
    private int codDisç;
    private String nomeDisc;

    private String tipoReg;

    public RegCrudMS() {
    }

    public int getCms() {
        return cms;
    }

    public void setCms(int cms) {
        this.cms = cms;
    }

    public int getCadastroAlunoDisciplina() {
        return cadastroAlunoDisciplina;
    }

    public void setCadastroAlunoDisciplina(int cadastroAlunoDisciplina) {
        this.cadastroAlunoDisciplina = cadastroAlunoDisciplina;
    }

    public int getCodDisç() {
        return codDisç;
    }

    public void setCodDisç(int codDisç) {
        this.codDisç = codDisç;
    }

    public String getNomeDisc() {
        return nomeDisc;
    }

    public void setNomeDisc(String nomeDisc) {
        this.nomeDisc = nomeDisc;
    }

    public String getTipoReg() {
        return tipoReg;
    }

    public void setTipoReg(String tipoReg) {
        this.tipoReg = tipoReg;
    }

}
